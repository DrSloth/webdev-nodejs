// JavaScript objects can be created on the fly and are very similiar to python dictionaries.

let obj = {age: 10, name: "Jack"};

// Output the object to the console
console.log(obj);

// Acces a field
console.log(obj.age);

// Add a field
obj["hobby"] = "Programming";

// Set a field
obj.age = 11;

console.log(obj);
