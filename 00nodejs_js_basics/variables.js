// In JavaScript variables can be defined with three keywords: let, const and var
// But only let and const are used nowadays, using var is highly discouraged

// Define a simple variable with let and an equals sign

// This defines a variables called x with the value of 10
let x = 10;
// Using this we reassign the value of 3 to x.
x = 3;

console.log(x);

// NOTE ending a line with a semicolon is optional

// You can also use const to make non reassignable variables
const y = "Hello";

// The following outcommented line is not allowed
// y = 3;

console.log(y);
