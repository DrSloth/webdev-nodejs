// In javascript there are most primitive data types we already know from python

// We can use integers, the following line prints 10:
console.log(10); 

// We can use floats, the following line prints 4.2:
console.log(4.2);

// We have bools with the values true and false
console.log(true);
console.log(false);

// Practically not a primitive, but still. We also have strings for any kind of text.
console.log("Hi, everyone!")

// All of these types can also be assigned to variables
